    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String descricao = addGeneroTextField.getText();
        int codigo = 0;
        Genero genero = new Genero(codigo, descricao);
        try{
            if(GeneroDAO.existe(genero)){
                JOptionPane.showMessageDialog(null,String.format("Genero %s j√° existe.", genero.getDescricao()));
                addGeneroTextField.setText("");
            }if(descricao.isBlank()){
                JOptionPane.showMessageDialog(null,"Preencha os campos.");
                
            }else{
                GeneroDAO.inserir(genero);{
                JOptionPane.showMessageDialog(null,String.format("Genero %s cadastrado!", genero.getDescricao()));
                addGeneroTextField.setText("");
                buscarGeneros();
                
                
            }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Tente mais tarde.");
            e.printStackTrace();
            
        }
    }//GEN-LAST:event_addButtonActionPerformed

